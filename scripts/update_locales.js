// Cleanup deprecated keys
const validNames = new Set();
for (const sectionKey in config) {
    const section = config[sectionKey];
    for (const itemKey in section) {
        if (section[itemKey].name) {
            validNames.add(section[itemKey].name);
        }
        // Keep descriptions valid
        if (section[itemKey].description) {
            validNames.add(section[itemKey].description);
        }
    }
}

function cleanObj(obj) {
    const result = {};
    for (const key in obj) {
        // Keep UI keys and special keys
        if (key.startsWith("ui_") || key.startsWith("msg_") || key.startsWith("heading_") || key.startsWith("desc_")) {
            result[key] = obj[key];
            continue;
        }
        
        // Keep valid config names
        if (validNames.has(key)) {
            result[key] = obj[key];
            continue;
        }
        
        // Prune keys that look like internal variable names (e.g. vConsoleWindowSize)
        // but are NOT in the validNames list.
        if (/^[a-z][A-Z]/.test(key)) {
             // console.log(`Pruning deprecated key: ${key}`);
             continue; 
        }

        // Keep everything else (generic dictionary terms that might be used elsewhere)
        result[key] = obj[key];
    }
    return result;
}

